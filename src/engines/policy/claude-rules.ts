// ─── .claude/workflow.md — 개발 워크플로우 & CLI 명령어 ───

function buildWorkflow(config: HarnessConfig): string {
  const lines: string[] = [];

  lines.push('# Development Workflow');
  lines.push('');

  lines.push('## Commands');
  lines.push('```bash');
  lines.push('harness analyze              # Define domain glossary & feature specs');
  lines.push('harness sync                 # Sync architecture rules & check violations');
  lines.push('harness sync --check         # Check-only mode (CI / pre-commit)');
  lines.push('harness sync --watch         # Auto-sync on config change (run with dev server)');
  lines.push('harness generate <type> <n>  # Scaffold: component, hook, util, service, model');
  lines.push('harness test                 # Run tests with self-healing feedback loop');
  lines.push('harness test --coverage      # Run with coverage reporting');
  lines.push('```');
  lines.push('');

  lines.push('## Scaffolding');
  lines.push('Always use `harness generate` to create new files:');
  lines.push('```bash');
  lines.push('harness generate component UserProfile');
  lines.push('harness generate hook useAuth');
  lines.push('harness generate service payment');
  lines.push('harness generate util format-date');
  lines.push('harness generate model user');
  lines.push('```');
  lines.push('');

  lines.push('## Pre-commit');
  lines.push(`- Linter: \`${config.development.linter}\` runs on staged files`);
  lines.push(`- Formatter: \`${config.development.formatter}\``);
  lines.push('- TypeScript type check: `tsc --noEmit`');
  lines.push('');

  lines.push('## Pre-push');
  lines.push('- Full test suite: `harness test`');

  return lines.join('\n') + '\n';
}
